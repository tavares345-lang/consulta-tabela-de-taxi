export const getPricePerKm = (): number => {
  try {
    const storedPrice = localStorage.getItem(PRICE_PER_KM_KEY);
    if (!storedPrice) return 5.00;
    
    const parsed = parseFloat(storedPrice);
    // Ensure we don't return NaN which would break the UI or reset to default unexpectedly
    return isNaN(parsed) ? 5.00 : parsed;
  } catch (error) {
    console.error("Error getting price per km", error);
    return 5.00;
  }
};

export const storePricePerKm = (price: number) => {
  try {
    localStorage.setItem(PRICE_PER_KM_KEY, price.toString());
  } catch (error) {
    console.error("Error storing price per km", error);
  }
};
